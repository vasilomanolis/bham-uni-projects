
/**
 * An interface Sortable with the public method signature for a method double
 * compareValue() that returns the maximal speed and the total amount of money
 * spent in their corresponding classes Car and Customer, respectively.
 * 
 * @author Vasileios Manolis
 * @version 13.11.2017
 */
public interface Sortable {

	/**
	 * 
	 * @return the maximal speed and the total amount of money spent in their
	 *         corresponding classes Car and Customer, respectively.
	 */
	double compareValue();

}
